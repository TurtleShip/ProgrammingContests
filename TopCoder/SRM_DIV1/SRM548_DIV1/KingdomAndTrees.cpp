vector<int> H;

bool isOkay(int X) {
    int prev = max(1, H[0] - X);
    for(int i=1; i < H.size(); i++) {
        if( H[i] <= prev && (H[i] + X) < (prev+1) ) return false;
        
        if(H[i] <= prev) prev++;
        else prev = max(prev+1, H[i] - X);
    }
    return true;
}

int KingdomAndTrees::minLevel(vector <int> heights) {
    H = heights;
	int low = 0;
    int high = 1000000000;
    int res = -1;
    
    while(low <= high) {
        int mid = (low + high) / 2;
        if(isOkay(mid)) {
            res = mid;
            high = mid - 1;
        } else low = mid + 1;
    }
    
    return res;
}

