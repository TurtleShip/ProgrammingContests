class MergersDivOne {
public:
	double findMaximum(vector <int> revenues);
};

double MergersDivOne::findMaximum(vector <int> revenues) {
	//greedy approach
    vector<double> V;
    double res = 0.0;
    int N = revenues.size();
    for(int i=0; i < N; i++)
        V.push_back( double(revenues[i]) );
    sort(V.begin(), V.end());
    res = (V[0] + V[1]) / 2.0;
    for(int i = 2; i < N; i++) {
        res = (res + V[i]) / 2.0;
    }
    
    return res;
}
 
