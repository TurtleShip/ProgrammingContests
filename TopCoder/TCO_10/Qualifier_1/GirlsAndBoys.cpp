int GirlsAndBoys::sortThem(string row) {
	
    int N = row.size();
    
    //Girls on left - Boys on right
    int one = 0;
    int g = 0;
    for(int i=0; i < N; i++)
    {
        if(row[i] == 'G')
        {
            one += i - g;
            g++;
        }
    }
    
    //Boys on left - Girls on right
    int two = 0;
    int b = 0;
    for(int i=0; i < N; i++)
    {
        if(row[i] == 'B')
        {
            two += i - b;
            b++;
        }
    }
    return min(one,two);
}

