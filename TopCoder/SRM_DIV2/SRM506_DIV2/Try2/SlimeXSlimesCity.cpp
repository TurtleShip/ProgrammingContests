/*
 Ans <= N
 
 Go through 0 .. N - 1 city.
 Fore each city, find out if there is anyway that it can be the resulting city name.

 
 */

int SlimeXSlimesCity::merge(vector <int> populations) {
    
	int N = populations.size();
    vector<ll> pp;
    for(int i=0; i < N; i++)
        pp.push_back(ll(populations[i]));
    
    int ans = 0;
    sort(pp.begin(), pp.end());
    
    for(int i=0; i < N; i++)
    {

        ll cur = pp[i];

        for(int j=(i-1); j >=0; j--)
            cur += pp[j];
        
        bool valid = true;
        for(int j=(i+1); j < N; j++)
        {
            if(cur >= pp[j]) cur += pp[j];
            else
            {
                valid = false;
                break;
            }
        }
        if(valid) ans++;
    }
    
    return ans;
}

