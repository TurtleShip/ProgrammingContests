class MakeSquare {
public:
	int minChanges(string);
};

int mem[55][55];
int leftLimit;
int N;
string str;

int rec(int x, int y)
{
    int& res = mem[x][y];
    
    if(res != -1) return res;
    
    res = 50000;
    if(x == leftLimit && y == N) res = 0;

    if(x < leftLimit && y < N)
    {
        if(str[x] == str[y]) res = rec(x+1,y+1);
        else res = 1 + rec(x+1,y+1);
    }
    
    // delete one from left , or add one to the right
    if(x < leftLimit)
        res = min(res, 1 + rec(x+1,y));
    
    if(y < N)
        res = min(res, 1 + rec(x, y+1));
    
    return res;
}

int MakeSquare::minChanges(string S) {
    str = S;
	N = str.size();
    
    int ans = 5000;
    
    for(leftLimit = 0; leftLimit <= N; leftLimit++)
    {
        memset(mem, -1, sizeof(mem));
        ans = min(ans, rec(0, leftLimit));
    }

    return ans;
}

