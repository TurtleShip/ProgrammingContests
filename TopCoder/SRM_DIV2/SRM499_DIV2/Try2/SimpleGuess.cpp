class SimpleGuess {
public:
	int getMaximum(vector <int>);
};

int SimpleGuess::getMaximum(vector <int> hints) {
	
    /*
     max(x+y) = 100
     min(x-y) = 0
     Runtime : 100*100* 50 = 500000. Small enough
     */
    int best = 0;
    for(int x=1; x <= 100; x++)
    {
        for(int y=0; y < x;y++)
        {
            int add = x+y;
            int dif = x-y;
            bool hasAdd = false;
            bool hasDif = false;
            for(int i=0; i < hints.size(); i++)
            {
                if(hints[i] == add) hasAdd = true;
                if(hints[i] == dif) hasDif = true;
            }
            if(hasAdd && hasDif)
                best = max(best, x*y);
        }
    }
    
    return best;
}

