class SimpleGuess {
public:
	int getMaximum(vector <int> hints);
};

int SimpleGuess::getMaximum(vector <int> hints){
	//The best value can be found by picking smallest possible number
    //and biggest possible number
    int X;
    int Y;
    int maxV = 0;
    sort(hints.begin(), hints.end());
    for(int i=0;i < hints.size(); i++)
    {
        for(int j= hints.size() - 1; j > i; j--)
        {
            int A = hints[j];
            int B = hints[i];
            if((A+B) % 2 != 0 || (A - B) % 2 != 0)
                continue;
            X = (A+B) / 2;
            Y = (A-B) / 2;
            maxV = max(maxV, X*Y);
        }
    }
    return maxV;
}
 

