vector <int> TheProgrammingContestDivTwo::find(int T, vector <int> req) {
	vector<int> ans(2,0);
    int N = req.size();
    int time = 0;
    sort(req.begin(), req.end());
    
    for(int i= 0; i < N; i++)
    {
        if( (time + req[i]) <= T)
        {
            time += req[i];
            ans[1] += time;
            ans[0]++;
        }
        else
            break;
    }
    return ans;
}

