int KingXNewCurrency::howMany(int A, int B, int X) {
    
	if(A%X == 0 && B%X == 0) return -1;
    int res = 0;
    for(int Y=1; Y <= max(A,B); Y++)
    {
        bool passA = (A%Y == 0);
        bool passB = (B%Y == 0);
        for(int x = X; x <= A; x +=X)
            if( (A-x) % Y == 0 ) passA = true;
        for(int x = X; x <= B; x +=X)
            if( (B-x) % Y == 0 ) passB = true;
        if(passA && passB)
            res++;
    }
    return res;
}

