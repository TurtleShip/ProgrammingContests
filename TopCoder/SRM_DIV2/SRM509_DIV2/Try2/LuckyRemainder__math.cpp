/*
 Let N = number of elements in X = length of X
 Say we are at index "idx."
 If we choose to include this, 
 there are 2^(N - 1) ways to make numbers using (N-1) other numbers.
 
 Also, take advantage of the fact that for all 0 <= i, 10^i % 9 =1
 */
int LuckyRemainder::getLuckyRemainder(string X) {
	int N = X.size();
    
    ll mult = 1ll<<(N-1);
   
    ll ans = 0;
    for(int i=0; i < N; i++)
    {
        ans += (X[i] - '0');
    }
    return (ans * mult) % 9;;
}

