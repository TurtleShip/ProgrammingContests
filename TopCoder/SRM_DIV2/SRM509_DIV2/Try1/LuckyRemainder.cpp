typedef long long ll;
 
int getNum(char c)
{
    return (c - '0');
}
class LuckyRemainder {
public:
	int getLuckyRemainder(string X);
};

int LuckyRemainder::getLuckyRemainder(string X){
    int N = X.size();
    int sum = 0;
    ll mult  = (ll)pow(2.0, N-1);
    for(int i=0;i < N; i++)
    {
        sum += getNum(X[i]) * mult % 9;
    }
    
    return sum % 9;

    
}
 

