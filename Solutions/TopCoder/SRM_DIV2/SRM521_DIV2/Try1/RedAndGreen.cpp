class RedAndGreen {
public:
	int minPaints(string row);
};

int RedAndGreen::minPaints(string row){
	int N = row.size();
    int curMin = 51;
    /* Go through each element
     Count how many greens < i <= how many reds. Those need to be repainted */
    for(int i=0; i <= N; i++)
    {
        int green = 0;
        int red = 0;
        for(int j=0; j < i; j++)
            if(row[j] == 'G') green++;
        for(int j=i; j < N; j++)
            if(row[j] == 'R') red++;
        curMin = min(curMin, green + red);
    }
    return curMin;
}
 

