bool isPalin(string str) {
    int N = str.size();
    if(N <= 1) return true;
    
    int i=0, j=N-1;
    while(i < j) {
        if(str[i] != str[j]) return false;
        i++;
        j--;
    }
    return true;
}

string replaceCopy(string str) {
    int prev = 0;
    string res = "";
    for(int i=1; i < str.size(); i++) {
        if(str[prev] != str[i]) {
            res  = res + str[prev];
            prev = i;
        }
    }
    res = res + str[prev];
    return res;
}

string FauxPalindromes::classifyIt(string word) {
	string palin = "PALINDROME";
    string faux = "FAUX";
    string notEven = "NOT EVEN FAUX";
    
    if(isPalin(word)) return palin;
    word = replaceCopy(word);
    if(isPalin(word)) return faux;
    return notEven;
}

