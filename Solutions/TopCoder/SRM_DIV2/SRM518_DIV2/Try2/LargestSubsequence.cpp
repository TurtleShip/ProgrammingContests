string LargestSubsequence::getLargest(string s) {
	int N = s.size();
    int pos = 0;
    string ans = "";

    while(pos < N)
    {
        char cur = s[pos];

        for(int i = (pos+1); i < N; i++)
            if(s[i] > cur)
            {
                pos = i;
                cur = s[i];
            }
        ans += cur;
        pos++;
    }
    return ans;
}

