int GogoXBallsAndBinsEasy::solve(vector <int> T) {
	int N = T.size();
    
    vector<int> T_rev;
    
    for(int i=N-1; i >=0; i--)
        T_rev.push_back(T[i]);

    
    int ans = 0;
    for(int i=0; i < N/2; i++)
        ans += (T_rev[i] - T[i]);
    
    return ans;
}

