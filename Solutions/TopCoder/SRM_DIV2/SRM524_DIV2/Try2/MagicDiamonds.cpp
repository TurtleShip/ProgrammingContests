//returns true if n is prime
bool isPrime(ll n)
{
	if(n <= 1ll) return false;
	if(n == 2ll) return true;
	if(n%2ll == 0)return false;
	ll m = (ll)sqrt(n);
	for(ll i=3; i <=m; i+=2)
		if(n%i == 0)
			return false;
	return true;
}
long long MagicDiamonds::minimalTransfer(long long n) {
	/*
     n is either prime or not.
     If it is not a prime, return 1.
     If it is not a prime, remember that every "even" number is a multiple of 2.
     So return 1(to make n a prime) + 1(to transport a prime) = 2

     Notice special cases.
     If n <= 3 return n. */
    
    if(n <= 3) return n;
    
    if(!isPrime(n)) return 1;
    return 2;
    
}

