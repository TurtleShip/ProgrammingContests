//a and b cannot both be 0
int GCD(int a, int b)
{
	if(b==0) return a;
	return GCD(b, a%b);
}

int LCM(int a, int b)
{
	return b * a / GCD(a,b);
}


double Starport::getExpectedTime(int N, int M) {
	
    int x = (N - (M%N));
    int G = GCD( x, N);
    return (N-G)/2.0;
}

