int NinePuzzle::getMinimumCost(string init, string goal) {
    int r=0,g=0,b=0,y=0;
    
    int N = 10;
    for(int i=0; i < N; i++)
    {
        if(init[i] == goal[i]) continue;
        if(init[i] == 'R') r++;
        if(init[i] == 'G') g++;
        if(init[i] == 'B') b++;
        if(init[i] == 'Y') y++;
        
        if(goal[i] == 'R') r--;
        if(goal[i] == 'G') g--;
        if(goal[i] == 'B') b--;
        if(goal[i] == 'Y') y--;
    }
    
    return (abs(r) + abs(g) + abs(b) + abs(y))/2;
}

