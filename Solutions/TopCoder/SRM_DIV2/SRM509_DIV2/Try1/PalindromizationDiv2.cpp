//can handle only num > 0
bool isPalindrome(int num)
{
    int cap = log10((double)num) + 1;
    int split[cap];
    for(int i=0; i < cap; i++)
    {
        split[i] = num % 10;
        num /= 10;
    }
        
    for(int i=0; i < cap/2; i++)
    {
        if(split[i] != split[cap - i - 1])
        return false;
    }
    return true;
} 
 
class PalindromizationDiv2 {
public:
	int getMinimumCost(int X);
};


int PalindromizationDiv2::getMinimumCost(int X) {
    if(X < 10) return 0;
    if(isPalindrome(X)) return 0;
    int money = 1;
    int minX = X - 1;
    int maxX = X + 1;
    while(!isPalindrome(minX) && !isPalindrome(maxX))
    {
        money++;
        minX = X - money;
        maxX = X + money;
       // cout<<"minX : "<<minX<<" , maxX : "<<maxX<<endl;
        minX = max(10, minX); 
    }
    
    return money;	
}
 

