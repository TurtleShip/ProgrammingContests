class CorrectMultiplicationTwo {
public:
	int getMinimum(int a, int b, int c);
};

int CorrectMultiplicationTwo::getMinimum(int a, int b, int c){
    ll curMin = a-1 + b-1 + c - 1;
    for(ll A = 1; A <= curMin + c; A++)
    {
        for(ll B = 1; B <= (curMin + c)/A; B++)
        {
            ll C = A * B;
            curMin = min(curMin, (abs(A-a) + abs(B-b) + abs(C-c)));
        }
    }
    return curMin;
	
}
 

