string NetworkXZeroOne::reconstruct(string message) {
	int N = message.size();
	int pos = 0;
	bool isX = false;
	for(int i=0; i < N; i++)
	if(message[i] != '?')
	{
		pos = i;
		if(message[i] == 'x')
			isX = true;
		break;
	}
	
	char fi = ((pos%2 == 0 && isX) || (pos%2 == 1 && !isX)) ? 'x' : 'o';
	char se = (fi == 'x') ? 'o' : 'x';
	for(int i=0; i < N; i++)
	{
		if(i % 2 == 0) message[i] = fi;
		else message[i] = se;
	}
	return message;
}



