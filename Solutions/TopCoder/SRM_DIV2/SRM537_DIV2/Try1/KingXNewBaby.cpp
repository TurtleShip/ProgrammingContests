string KingXNewBaby::isValid(string name) {
	int N = name.size();
    
    if(N != 8) return "NO";
    
    char vowels[8];
    int sz = 0;
    for(int i=0; i < N; i++)
        if(isVowel(name[i]))
            vowels[sz++] = name[i];
    
    if( sz == 2 && vowels[0] == vowels[1])
        return "YES";
    return "NO";
}

bool isVowel(char ch)
{
    string str = "aeiou";
    for(int i=0; i < 5; i++)
        if(ch == str[i]) return true;
    return false;
}

