bool isAlmostLuck(int num)
{
    stringstream ss;
    ss<<num;
    string str;
    ss>>str;
    int cnt = 0;
    for(int i=0; i < str.size(); i++)
    {
        if(str[i] != '4' && str[i] != '7')
        {
            cnt++;
            if(cnt > 1) return false;
        }
    }
    return (cnt <= 1);
}

int TheAlmostLuckyNumbersDivTwo::find(int a, int b) {
    int ans = 0;
	for(int i=a; i <=b; i++)
        if(isAlmostLuck(i))
            ans++;
    return ans;
}

