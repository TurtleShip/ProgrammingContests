long long FoxPaintingBalls::theMax(long long R, long long G, long long B, int _N) {
    ll N = ll(_N);
    ll perTri = N*(N+1)/6ll;
    ll res = 0ll;
    
    if(N == 1) return R + G + B;
    if(N%3 != 1) return min( R, min(G,B) ) / perTri;
    
    ll l = 0ll;
    ll r = R+G+B;
    ll m = 0ll;
    ll minVal = min(R, min(G,B));
    while(l <= r) {
        m = (l+r)/2ll;
        if(minVal/perTri >= m && (R+G+B - 3*perTri*m >= m)) {
            res = m;
            l = m + 1;
        }
        else r = m - 1;
    }
    
    return res;
}

