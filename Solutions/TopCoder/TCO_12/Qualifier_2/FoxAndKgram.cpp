int FoxAndKgram::maxK(vector <int> leng) {
    
    len = leng;
    N = len.size();
    int res = 0;
    
    for(int k=1; k <= N; k++)
        if(isPossible(k))
            res = k;
    
    return res;
}

//return true if k is a valid k-gram value
bool isPossible(int k)
{
    int res = 0;
    vector<bool> used(N,false);
    
    //find Yang
    for(int i=0; i < N; i++)
        if(len[i] == k)
        {
            used[i] = true;
            res++;
        }
    
    //find Yin
    for(int i=0; i < N; i++)
        for(int j=i+1; j < N; j++)
            if(!used[i] && !used[j] && (len[i] + len[j] + 1) == k)
            {
                used[i] = used[j] = true;
                res++;
            }
    
    return res >= k;
}
