int PasswordXPalindrome::minSwaps(string pwd) {
    
    int N = pwd.size();
    int res = 0;
    bool soloChk = false;
    for(int i = 0; i < N/2; i++)
    {
        if(pwd[i] == pwd[N-1-i]) continue;
        bool found = false;
        for(int j=i+1; !found && j < N-1-i; j++)
            if(pwd[i] == pwd[j])
            {
                swap(pwd[j], pwd[N-1-i]);
                res++;
                found = true;
            }
        if(!found)
        {
            if(N%2 == 1 && !soloChk)
            {
                swap(pwd[i], pwd[N/2]);
                res++;
                soloChk = true;
                i--;
            }
            else if(pwd[i] != pwd[N-1-i])
                return -1;
        }
    }
	return res;
}

