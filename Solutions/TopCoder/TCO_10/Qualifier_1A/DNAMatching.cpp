class DNAMatching {
public:
	int getMaxSize(vector <string>);
};

int DNAMatching::getMaxSize(vector <string> dna) {

    int N = dna.size();
    int ans = N;
    for(int i=0; i < N; i++)
    {
        string eq = dna[i];
        reverse(eq.begin(), eq.end());
        for(int j=0; j < eq.size(); j++)
        {
            if(eq[j] == 'A') eq[j] = 'T';
            else if(eq[j] == 'T') eq[j] = 'A';
            else if(eq[j] == 'C') eq[j] = 'G';
            else if(eq[j] == 'G') eq[j] = 'C';
        }
        for(int j=i+1; j < N; j++)
            if(dna[j] == eq)
            {
                ans--;
                break;
            }
    }
    return ans;
}


