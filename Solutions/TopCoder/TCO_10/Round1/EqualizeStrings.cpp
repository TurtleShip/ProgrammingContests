string EqualizeStrings::getEq(string s, string t) {
	int N = s.size();
    string ans(N, '-');
    
    for(int i=0; i < N; i++)
    {
        int val = 500;
        char best = '!';
        for(char ch = 'a'; ch <= 'z'; ch++)
        {
            int ds = max(ch,s[i]) - min(ch,s[i]);
            int dt = max(ch,t[i]) - min(ch,t[i]);
            int cur = min(ds, 26-ds) + min(dt, 26-dt);
            if(cur < val)
            {
                val = cur;
                best = ch;
                
            }
        }
        ans[i] = best;
    }
    
    return ans;
}

